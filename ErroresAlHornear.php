        <h3 class="mt-4 mb-2">Errores frecuentes y soluciones</h3>
        <ul class="blog-list">
          <li>
            <strong>El pastel no sube:</strong> Puede deberse a polvo de hornear vencido, temperatura baja o exceso de mezcla. Usa ingredientes frescos y mezcla solo lo necesario.
          </li>
          <li>
            <strong>El bizcocho se hunde en el centro:</strong> Abrir el horno antes de tiempo o exceso de líquido en la mezcla. No abras el horno antes de los 30 minutos y sigue la receta al pie de la letra.
          </li>
          <li>
            <strong>El pastel queda seco:</strong> Hornear demasiado tiempo o a temperatura alta. Usa un temporizador y revisa con un palillo antes de tiempo.
          </li>
          <li>
            <strong>El pastel se pega al molde:</strong> No engrasar/enharinar bien el molde o desmoldar en caliente. Usa papel manteca o engrasa bien y espera a que enfríe un poco antes de desmoldar.
          </li>
          <li>
            <strong>Galletas duras o quemadas:</strong> Exceso de tiempo en el horno o bandeja muy caliente. Usa papel para hornear y revisa el tiempo recomendado.
          </li>
          <li>
            <strong>Pan poco esponjoso:</strong> No dejar levar suficiente o usar levadura vieja. Respeta los tiempos de levado y usa levadura fresca.
          </li>
        </ul>
