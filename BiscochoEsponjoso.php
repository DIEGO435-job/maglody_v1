        <h2 class="mb-3">¡Prepara el bizcocho más esponjoso y delicioso!</h2>
        <p>
          El bizcocho es la base de muchos postres y tortas. Aquí te enseñamos una receta sencilla, con ingredientes fáciles de conseguir y algunos tips para que siempre te quede perfecto, suave y aireado.
        </p>
        <h3 class="mt-4 mb-2">Ingredientes</h3>
        <ul class="blog-list">
          <li>4 huevos grandes (a temperatura ambiente)</li>
          <li>200 g de azúcar (1 taza)</li>
          <li>200 g de harina de trigo (1 1/2 tazas aprox.)</li>
          <li>80 ml de leche (1/3 taza)</li>
          <li>80 ml de aceite vegetal (1/3 taza)</li>
          <li>1 cucharada de esencia de vainilla</li>
          <li>1 cucharada de polvo de hornear</li>
          <li>Pizca de sal</li>
        </ul>
        <h3 class="mt-4 mb-2">Preparación paso a paso</h3>
        <ol class="blog-list">
          <li>Precalienta el horno a 180°C. Engrasa y enharina un molde redondo de 22 cm.</li>
          <li>En un bol, bate los huevos con el azúcar hasta que la mezcla esté muy espumosa y clara (puedes usar batidora eléctrica, unos 5-7 minutos).</li>
          <li>Agrega la esencia de vainilla y la pizca de sal. Mezcla suavemente.</li>
          <li>Incorpora la leche y el aceite en forma de hilo, batiendo a baja velocidad.</li>
          <li>Tamiza la harina junto con el polvo de hornear e incorpóralos en dos partes, mezclando con movimientos envolventes para no perder el aire.</li>
          <li>Vierte la mezcla en el molde y da unos golpecitos suaves para eliminar burbujas grandes.</li>
          <li>Hornea durante 35-40 minutos o hasta que al insertar un palillo salga limpio.</li>
          <li>Deja enfriar 10 minutos, desmolda y deja enfriar completamente sobre una rejilla.</li>
        </ol>
        <h3 class="mt-4 mb-2">Tips de expertos</h3>
        <ul class="blog-list">
          <li>Usa huevos a temperatura ambiente para que monten mejor.</li>
          <li>No sobrebatas la harina, mezcla solo lo necesario para integrar.</li>
          <li>Si quieres un bizcocho aún más aireado, separa claras y yemas, bate las claras a punto de nieve y agrégalas al final con movimientos envolventes.</li>
          <li>Evita abrir el horno antes de los 30 minutos para que no se baje.</li>
        </ul>
